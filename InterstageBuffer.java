public class InterstageBuffer
{
	private String RA;
	private String RB;
	private String RM;
	private String RY;
	private String RZ;
	private String IR;
	private String PC_Temp;
	
	public InterstageBuffer()
	{
		RA = RB = RM = RY = RZ = IR = PC_Temp = "";
	}
	
	// RA
	public String getRA()
	{
		return RA;
	}
	
	public void setRA(String val)
	{
		RA = val;
	}
	
	// RB
	public String getRB()
	{
		return RB;
	}
	
	public void setRB(String val)
	{
		RB = val;
	}
	
	// RM
	public String getRM()
	{
		return RM;
	}
	
	public void setRM(String val)
	{
		RM = val;
	}
	
	// RY
	public String getRY()
	{
		return RY;
	}
	
	public void setRY(String val)
	{
		RY = val;
	}
	
	// RZ
	public String getRZ()
	{
		return RZ;
	}
	
	public void setRZ(String val)
	{
		RZ = val;
	}
	
	// IR
	public String getIR()
	{
		return IR;
	}
	
	public void setIR(String val)
	{
		IR = val;
	}
	
	// PC_Temp
	public String getPC_Temp()
	{
		return PC_Temp;
	}
	
	public void setPC_Temp(String val)
	{
		PC_Temp = val;
	}
	
	
	
	
	
}